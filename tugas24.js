function urutBalik() {
    const data = [32, 33, 16, 40, 9, 7, 11, 2, 44, 1, 66];
    console.log('Sebelumnya: ' + data);

    // sort:
    let ascend = data.sort();
    console.log('Ascending: ' + ascend);

    // reverse:
    let descend = data.reverse();
    console.log('Descending: ' + descend);
}

urutBalik();